#include "frankenshell.h"

t_bool	ft_isqoute(char c)
{
	return (c == '\'' || c == '"');
}

t_bool	ft_issep(char c)
{
	return (c == '|' || c == '>' || c == '<');
}

t_bool	ft_isspace(char c)
{
	return (c == ' ' || c == '\n' || c == '\t' || c == '\v' || c == '\a'
		|| c == '\b' || c == '\f' || c == '\r');
}
